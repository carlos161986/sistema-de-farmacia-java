    private void jbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Cliente cliente = new Cliente();
            ResultSet reg = cliente.BuscarPorNOmbre(txtBuscar.getText());
            String[] columnNames = new  String[]{"Codigo","Nombre","Paterno","Materno","Direccion","Telefono","Correo"};
            Object[][]data = new Object[25][7];
            int i = 0;
            while (reg.next()){
                    data[i][0] = reg.getInt(1);
                    data[i][1] = reg.getString(2);
                    data[i][2] = reg.getString(3);
                    data[i][3] = reg.getString(4);
                    data[i][4] = reg.getString(5);
                    data[i][5] = reg.getString(6);
                    data[i][6] = reg.getString(7);
                    i++;
            }
            DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
            tablaCliente.setModel(tabla);
        }catch(Exception e){}

    }//GEN-LAST:event_jbtnBuscarActionPerformed

