    private void tablaClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaClienteKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 10) {
            txtCod.setVisible(true);
            lblcod.setVisible(true);
            int fila = tablaCliente.getSelectedRow();
            if (fila >= 0 && tablaCliente.getValueAt(fila, 0) != null) {
                txtCod.setText(String.valueOf(tablaCliente.getValueAt(fila, 0)));
                txtNombre.setText((String) tablaCliente.getValueAt(fila, 1));
                txtPaterno.setText((String) tablaCliente.getValueAt(fila, 2));
                txtMaterno.setText((String) tablaCliente.getValueAt(fila, 3));
                txtDireccion.setText((String) tablaCliente.getValueAt(fila, 4));
                txtTelefono.setText((String) tablaCliente.getValueAt(fila, 5));
                txtCorreo.setText((String) tablaCliente.getValueAt(fila, 6));

                //this.dispose();
            } else {
                label.setText("");
                txtNombre.setText("");
                txtPaterno.setText("");
                txtMaterno.setText("");
                txtDireccion.setText("");
                txtTelefono.setText("");
                txtCorreo.setText("");
            }
        }

    }//GEN-LAST:event_tablaClienteKeyPressed

