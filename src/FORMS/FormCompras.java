    private void tablaMedicamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMedicamentoMouseClicked
        // TODO add your handling code here:
        SeleccionarAlDetalle();
        int i = 0;
        double total=0;
        while(i < tablaDetalle.getRowCount()){
            total += Double.parseDouble(String.valueOf(tablaDetalle.getValueAt(i,3))) * Double.parseDouble(String.valueOf(tablaDetalle.getValueAt(i,2)));
            tablaDetalle.setValueAt(Double.parseDouble(String.valueOf(tablaDetalle.getValueAt(i,3))) * Double.parseDouble(String.valueOf(tablaDetalle.getValueAt(i,2))), i, 4);
            i++;
        }
        txtTotal.setText(String.valueOf(total));
    }//GEN-LAST:event_tablaMedicamentoMouseClicked

