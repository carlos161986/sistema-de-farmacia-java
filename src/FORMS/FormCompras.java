    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         if(tablaDetalle.getRowCount() > 0){
            compra.cod_provee(Integer.parseInt(txtProveedorCodigo.getText()));
            compra.Cod_emp(Integer.parseInt(txtCodEmpleado.getText()));
            compra.Guardar();
            int i = 0;
            while(i < tablaDetalle.getRowCount()){
                detalle.CodigoMedicamento(Integer.parseInt(String.valueOf(tablaDetalle.getValueAt(i,0))));
                detalle.Cantidad(Integer.parseInt(String.valueOf(tablaDetalle.getValueAt(i,3))));
                detalle.Costo(Double.parseDouble(String.valueOf(tablaDetalle.getValueAt(i,2))));
                detalle.Guardar();
                i++;
            }
            JOptionPane.showMessageDialog(null, "Venta Realizada Correctamente");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

