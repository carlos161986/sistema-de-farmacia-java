    public void Buscar() {
        Medicamento medicamento = new Medicamento();
        boolean sw = false;
        if (jComboBox1.getItemCount() > 1) {
            sw = true;
            try {
                if(jComboBox1.getSelectedItem()=="Ninguno")
                {
                    String[] columnNames = new String[]{"Codigo", "Nombre", "Precio", "Stock"};
                    int i=0;
                    Object[][] data = new Object[25][4];
                    while(i<25){
                     data[i][0] = "";
                    data[i][1] = "";
                    data[i][2] = "";
                    data[i][3] = "";
                    i++;
                    }
                    DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
                tablaMedicamento.setModel(tabla);
                }
                else{
                int key = ((Categoria) jComboBox1.getSelectedItem()).Codigo();
                ResultSet reg = medicamento.traerMedicamento(txtBuscar.getText(), String.valueOf(key), sw);
                String[] columnNames = new String[]{"Codigo", "Nombre", "Precio", "Stock"};
                Object[][] data = new Object[500][4];
                int i = 0;
                while (reg.next()) {
                    data[i][0] = reg.getInt(1);
                    data[i][1] = reg.getString(2);
                    data[i][2] = reg.getDouble(3);
                    data[i][3] = reg.getInt(4);
                    i++;
                }
                DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
                tablaMedicamento.setModel(tabla);}

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

    ArrayList lista = new ArrayList();
    public void SeleccionarAlDetalle() {
        int fila = tablaMedicamento.getSelectedRow();
        if (fila < 0 && !lista.contains(tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 0))) {
            return;
        }
        Object[][] data = new Object[tablaDetalle.getRowCount() + 1][5];
        int i = 0;
        while (i < tablaDetalle.getRowCount()) {
            data[i][0] = tablaDetalle.getValueAt(i, 0);
            data[i][1] = tablaDetalle.getValueAt(i, 1);
            data[i][2] = tablaDetalle.getValueAt(i, 2);
            data[i][3] = tablaDetalle.getValueAt(i, 3);
            data[i][4] = tablaDetalle.getValueAt(i, 4);
            i++;
        }
        if (!lista.contains(tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 0))) {
            lista.add(tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 0));
            data[i][0] = tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 0);
            data[i][1] = tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 1);
            data[i][2] = tablaMedicamento.getValueAt(tablaMedicamento.getSelectedRow(), 2);
            data[i][3] = 1;
        } else {
            return;//new Object[][]{}
        }
        String[] columnNames = new String[]{"Codigo", "Nombre", "Precio", "Cantidad","Sub Total"};
        tablaDetalle.setModel(new javax.swing.table.DefaultTableModel(
                data, columnNames) {

            Class[] types = new Class[]{
                java.lang.Integer.class, java.lang.String.class, java.lang.Double.class, java.lang.Integer.class,java.lang.Double.class
            };
            boolean[] canEdit = new boolean[]{
                false, false, true, true
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });

    }

