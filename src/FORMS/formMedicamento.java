void buscar(){
    try {
                Medicamento med = new Medicamento();
                ResultSet reg = med.buscarmedicamento(txtBuscar.getText());
                String[] columnNames = new String[]{"Codigo", "Nombre", "Stock", "Precio", "Categoria"};
                Object[][] data = new Object[150][5];
                int i = 0;
                while (reg.next()) {
                    data[i][0] = reg.getInt(1);
                    data[i][1] = reg.getString(2);
                    data[i][2] = reg.getString(3);
                    data[i][3] = reg.getString(4);
                    data[i][4] = reg.getString(5);
                    i++;
                }
                DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
                tablaMedicamentdo.setModel(tabla);
            } catch (Exception e) {
        }
}
