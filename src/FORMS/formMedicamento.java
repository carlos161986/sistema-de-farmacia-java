    private void tablaMedicamentdoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMedicamentdoMouseClicked
        // TODO add your handling code here:
        int fila = tablaMedicamentdo.getSelectedRow();
            if (fila >= 0 && tablaMedicamentdo.getValueAt(fila, 0) != null) {
                txtcod.setText(String.valueOf(tablaMedicamentdo.getValueAt(fila, 0)));
                txtNombre.setText((String) tablaMedicamentdo.getValueAt(fila, 1));
                txtprecio.setText((String) tablaMedicamentdo.getValueAt(fila, 2));
                txtstock.setText((String) tablaMedicamentdo.getValueAt(fila, 3));
                txtcat.setText((String) tablaMedicamentdo.getValueAt(fila, 4));
            } else {
                txtcod.setText("");
                txtNombre.setText("");
                txtprecio.setText("");
                txtstock.setText("");
                txtcat.setText("");
                txtcategoria.setText("");
            }
        buscat();
    }//GEN-LAST:event_tablaMedicamentdoMouseClicked

    public void buscat(){
        try {
                Categoria cat = new Categoria();
                ResultSet reg = cat.traerCategoria();
                String[] columnNames = new String[]{"Codigo", "Categoria"};
                Object[][] data = new Object[30][2];
                int i = 0;
                while (reg.next()) {
                    data[i][0] = reg.getInt(1);
                    data[i][1] = reg.getString(2);
                    i++;
                }
                DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
                TablaCategoria.setModel(tabla);
            } catch (Exception e) {
        }
    }
