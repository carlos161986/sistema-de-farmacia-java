    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Empleado empleado = new Empleado();
            ResultSet reg = empleado.buscarPorNombre(txtBuscar.getText());
            String[] columnNames = new  String[]{"Codigo","Nombre","Paterno"};
            Object[][]data = new Object[25][7];
            int i = 0;
            while (reg.next()){
                    data[i][0] = reg.getInt(1);
                    data[i][1] = reg.getString(2);
                    data[i][2] = reg.getString(3);
                    i++;
            }
            DefaultTableModel tabla = new DefaultTableModel(data, columnNames);
            tablaEmpleado.setModel(tabla);
        }catch(Exception e){}
    }//GEN-LAST:event_btnBuscarActionPerformed

