    private void tablaEmpleadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaEmpleadoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 10) {
            if (evt.getKeyCode() == 10) {
                int fila = tablaEmpleado.getSelectedRow();
                if (fila >= 0 && tablaEmpleado.getValueAt(fila, 0) != null) {
                    txtCodEmpleado.setText(String.valueOf(tablaEmpleado.getValueAt(fila, 0)));
                    txtEmpleado.setText((String) tablaEmpleado.getValueAt(fila, 1));
                    this.dispose();
                } else {
                    txtCodEmpleado.setText("");
                    txtEmpleado.setText("");
                }
            }
        }
    }//GEN-LAST:event_tablaEmpleadoKeyPressed

